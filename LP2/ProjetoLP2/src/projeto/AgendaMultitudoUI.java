    private String[] getNomesEventos(String parametro, String nomeEvento) throws IOException {
    	String[] nomesEventos = new String[0]; 
    	if (! (isArquivoExistente(ARQUIVO_AGENDA))) {
    		java.awt.EventQueue.invokeLater(new Runnable() {

    			public void run() {
    				new Erro3UI().setVisible(true);
    			}
    		});
    	}
    	else {
    		ControladorArquivos c = new ControladorArquivos();
    		Agenda agenda = (Agenda) c.leObjetoNoArquivo(ARQUIVO_AGENDA);

    		if (parametro.equals("padrao")){
    			int cont = 0;
    			Iterator<Evento> it = agenda.iteratorDeEventos();
    			while (it.hasNext()) {
    				Evento e = (Evento) it.next();
    				nomesEventos[cont] = e.getNome();
    				cont++;
    			}
    		}

    		else if (parametro.equals("todos")) {
    			List<Evento> eventosComEsseNone = agenda.pesquisaEventos(nomeEvento);
    			nomesEventos = new String[eventosComEsseNone.size()];
    			int contador = 0;

    			for (Evento e : eventosComEsseNone) {
    				nomesEventos[contador] = e.getNome();
    				contador++;
    			}
    		}
    		else if (parametro.equals("futuros")) {
    			List<Evento> eventosFuturos = agenda.eventosFuturos();
    			nomesEventos = new String[eventosFuturos.size()];
    			int contador = 0;

    			for (Evento e : eventosFuturos) {
    				nomesEventos[contador] = e.getNome();
    				contador++;
    			}
    		}

    		else if (parametro.equals("passados")) {
    			List<Evento> eventosPassados = agenda.eventosPassados();
    			nomesEventos = new String[eventosPassados.size()];
    			int contador = 0;

    			for (Evento e : eventosPassados) {
    				nomesEventos[contador] = e.getNome();
    				contador++;
    			}
    		}

    	}
    	return nomesEventos;
    }
    
    
    private static boolean isArquivoExistente(String nomeDoArquivo) {
		File arq = new File(nomeDoArquivo);
		if (arq.exists())
			return true;
		return false;
	}
    
