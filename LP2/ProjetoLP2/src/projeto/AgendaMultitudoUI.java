    private String mostrarDia() {
        String[] dias = {"", "Domingo", "Segunda-feira", "Terça-feira", "Quarta-feira", "Quinta-feira", "Sexta-feira", "Sabado"};
        GregorianCalendar c = new GregorianCalendar();
        return dias[c.get(GregorianCalendar.DAY_OF_WEEK)];
    }

    private String mostrarData() {
        GregorianCalendar c = new GregorianCalendar();
        return c.get(GregorianCalendar.DAY_OF_MONTH) + "/" + c.get(GregorianCalendar.MONTH) + "/" + c.get(GregorianCalendar.YEAR);
    }
    
